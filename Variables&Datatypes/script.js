// Declare variables of different types
let name = "John Doe"; // string
let age = 25; // number
let isStudent = true; // boolean
let courses = ["Math", "Science", "History"]; // array
let person = { name: "John", age: 25, isStudent: true }; // object

// Output the values and their types
console.log("Name:", name, "(Type:", typeof name + ")");
console.log("Age:", age, "(Type:", typeof age + ")");
console.log("Is student:", isStudent, "(Type:", typeof isStudent + ")");
console.log("Courses:", courses, "(Type:", typeof courses + ")");
console.log("Person Object:", person, "(Type:", typeof person + ")");
